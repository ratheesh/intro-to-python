# Logical operators
# (and, or, not)

print("and operator")
print(True and True)
print(True and False)
print(False and True)
print(False and False)

print('---------------')
print("or operator")
print(True or True)
print(True or False)
print(False or True)
print(False or False)

print('---------------')
print("not operator")
print(not True)
print(not False)